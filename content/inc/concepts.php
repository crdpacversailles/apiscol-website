		<div class="span3">
			<h3>
				<span class="badge-warning">&nbsp;1 </span>&nbsp;Les ressources
			</h3>
			<img class="pull-left" alt="Resource vue comme une boite de conserve"
				src="img/tin.png" class="img-polaroid">
			<p>Les ressources pédagogiques sont les contenus numériques que les
				professeurs utilisent tous les jours avec leurs élèves : un texte au
				format .doc, une vidéo, une animation flash, une page web...</p>
			<p class="alert">Une ressource est définie par un point d'entrée
				unique. Ainsi un exercice interactif avec de très nombreux fichiers
				peut constituer une seule ressource. </p>
			<p>
				Pour en savoir plus, <a
					href="http://www.imsglobal.org/content/packaging/cpv1p2pd2/imscp_infov1p2pd2.html">IMS
					Content Packaging Information Model</a>
			</p>
		</div>
		<div class="span4">
			<h3>
				<span class="badge-info">&nbsp;2 </span>&nbsp;Les séquences
			</h3>
			<img class="pull-left"
				alt="Séquence vue comme un ensemble d'emballages" src="img/tins.png"
				class="img-polaroid">
			<p>Les professeurs ne se contentent pas de distribuer des ressources :
				ils le font dans un certain ordre, avec certaines intentions, et
				assignent aux élèves des activités. Les ressources ainsi
				structurées, assorties de finalités pédagogiques, forment des
				séquences.</p>
			<p class="alert alert-success">Dans ApiScol, on voit la séquence de
				la même façon, qu'elle ait été conçue pour l'enseignement présentiel
				ou pour l'apprentissage à distance. C'est vrai aussi hors du monde
				numérique : un manuel qui sert de support de travail en classe, ne
				peut-il pas aussi être utilisé parfois en autonomie ?</p>

		</div>
		<div class="span4">
			<h3>
				<span class="badge-success">&nbsp;3 </span>&nbsp;Les métadonnées
			</h3>
			<img class="pull-left" alt="Métadonnées vues comme des étiquettes"
				src="img/etiquette.png" class="img-polaroid">
			<p>Les métadonnées sont comparables à des étiquettes : si des
				emballages n'avaient pas d'étiquettes, il faudrait les ouvrir pour
				savoir ce qu'ils contiennent. On mettrait un temps considérable à
				faire ses courses...</p>
			<p>Sur cette étiquette, sont notés le titre, une description, mais
				aussi que la ressources est pour le programme de SVT de 6ème,
				chapitre 3, qu'elle est prévue pour durer 15 minutes, etc.</p>
			<p class="alert alert-warning">Dans une séquence, chaque ressource a
				des métadonnées, mais la séquence en tant que telle en a aussi.</p>
		</div>
		<div class="span12 hero-unit">
			<h2>Mettre en place une gestion de ressources pédagogiques</h2>
			<p>La plupart des ressources numériques scolaires sont des pages de
				site web ou des fichiers téléchargeables depuis ces pages. Souvent,
				un document de type &laquo;fiche séquence&raquo; les accompagne.</p>
			<p>Cette situation présente divers inconvénients :</p>
			<ul class="unstyled">
				<li><span class="badge-success">&nbsp;1 </span>&nbsp;Les ressources
					pédagogiques étant dispersées dans des sites, on peut difficilement
					les échanger, les transférer, les sauvegarder.</li>
				<li><span class="badge-warning">&nbsp;2 </span>&nbsp;Les
					possibilités de chercher dans les ressources sont limitées. Les
					utilisateur passent par des moteurs de recherche généralistes.</li>
				<li><span class="badge-info">&nbsp;3 </span>&nbsp;Il est plus ou
					moins impossible d'indexer soi-même les ressources selon le
					standard Sco-LOM-fr quand on les publie. Il faut aller le faire
					dans un catalogue central que les usagers du site ne voient pas.</li>
			</ul>
		</div>
		<div class="span3">
