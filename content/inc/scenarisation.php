			<h3>Pour quoi faire ?</h3>
			<p>
				Dans le web d'aujourd'hui, ou &laquo;web des documents&raquo;, les
				descriptions de ressources sont généralements des articles de CMS (<em>Content
					Management System</em> : Spip, Drupal, Joomla, etc) et les
				documents d'accompagnement sont des documents bureautiques
				(&laquo;fiche séquence&raquo;, etc.).
			</p>
			<p>
				Dans le web de demain, ou <a
					href="http://fr.wikipedia.org/wiki/Web_des_donn%C3%A9es">&laquo;web
					des données&raquo;</a>, les métadonnées et les données
				d'accompagnement pédagogiques sont exprimées dans des formats
				standard compréhensibles à la fois par les humains et par les
				ordinateurs.
			</p>
